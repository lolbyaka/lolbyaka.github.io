<br><br><span style="text-align: center !important; display: block; font-weight: 500;">4 Гарантии безопасности</span>
<br><br>4.1 Платежная система Wallet One лицензируема надзорным органом в каждой стране присутствия, а значит, полностью безопасна. Wallet One использует стандарт безопасности PCI DSS, SSL-протокол, системы безопасности Verified by Visa и MasterCard SecureCode. PCI DSS — международный стандарт безопасности, разработанный Советом по стандартам безопасности индустрии платежных карт (Payment Card Industry Security Standards Council). Представляет собой 12 требований к построению и обслуживанию безопасных систем и принят крупнейшими международными платежными системами. Wallet One ежегодно проходит тестирование на соответствие стандарту PCI DSS. SSL-протокол (Secure Sockets Layer) — криптографический транспортный механизм, обеспечивающий безопасность передачи данных. Verified by Visa — технология дополнительной защиты при проведении платежей, разработанная платежной системой Visa. MasterCard SecureCode — дополнительная защита при проведении платежей с банковских карт MasterCard. А еще мы используем антифрод-систему, предназначенную для выявления мошеннических платежей. Это делает Wallet One удобной и безопасной платежной системой для всех, кроме мошенников. В случае, если у Вас есть вопросы по совершенному платежу, Вы можете обратиться в службу поддержки клиентов по электронной почте support@cloudpayments.ru.
